def btnNext_clicked():
    global flipflop
    global key
    global value
    global mode
    if flipflop == True:
        #определяем выбранный словарь
        dictionary_val = radSelected.get()

        #определяем режим
        mode = radEngRus.get()

        #получаем ключ-значение
        if dictionary_val == 0:
            key = random.choice(list(dict800.keys()))
            value = dict800[key]
        elif dictionary_val == 3000:
            key = random.choice(list(dict3000.keys()))
            value = dict3000[key]
        # вывод ключа или значения
        if mode == 0:
            lbl.configure(text=key)
        elif mode == 1:
            lbl.configure(text=value)

        #прячем нижнюю надпись
        lblAnswer.configure(text='')
        flipflop = False
    elif flipflop == False:
        #вывод значения или ключа
        if mode == 0:
            lblAnswer.configure(text=value)
        elif mode == 1:
            lblAnswer.configure(text=key)
        flipflop = True

